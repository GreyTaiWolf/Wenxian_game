export const stageCoeff: Record<RealmPhaseId, number> = {
  early: 0.85,
  middle: 1,
  late: 1.15,
  peak: 1.3,
};

export function getWeaponAttackRange(realmTier: MajorRealmId, quality: ItemGrade, phase: RealmPhaseId = "middle"): [number, number] {
  const range = weaponAttackRanges[realmTier][quality];
  const coeff = stageCoeff[phase];
  return [Math.max(1, Math.round(range[0] * coeff)), Math.max(1, Math.round(range[1] * coeff))];
}
